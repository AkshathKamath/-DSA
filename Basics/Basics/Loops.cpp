#include <bits/stdc++.h>
using namespace std;

int main()
{
    for (int i = 0; i < 5; i++)
    {
        cout << "AK";
    }

    int i = 0;
    while (i < 5)
    {
        cout << "AK";
        i++;
    }

    int j = 1;
    do
    {
        cout << "Do while";
        j++;
    } while (j <= 0);

    return 0;
}